package org.tiankafei.ui.chart.dto;

/**
 * 自定义图形控件参数对象
 *
 * @author 甜咖啡
 */
public class TiankafeiChartDTO {

    /**
     * 图形界面的标题
     */
    private String charTitle;

    /**
     * 获取图形界面的标题
     *
     * @return 图形界面的标题
     */
    public String getCharTitle() {
        return charTitle;
    }

    /**
     * 设置图形界面的标题
     *
     * @param charTitle 图形界面的标题
     */
    public void setCharTitle(String charTitle) {
        this.charTitle = charTitle;
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
